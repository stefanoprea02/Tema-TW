function readMore1(){
    document.querySelector('#content1').style.height= 'auto';
    document.querySelector('#readMore1').style.display = 'none';
    document.querySelector('#readLess1').style.display = 'block';
}
function readLess1(){
    document.querySelector('#content1').style.height= '2em';
    document.querySelector('#readMore1').style.display = 'block';
    document.querySelector('#readLess1').style.display = 'none';
}

